int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0' && str[i] > 47 && str [i] < 58)
		i++;
	if (str[i] == '\0')
		return (1);
	else
		return (0);
}
