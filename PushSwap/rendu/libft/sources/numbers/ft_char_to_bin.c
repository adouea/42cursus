#include "libft.h"

void	ft_char_to_bin(char c, int *bin_char)
{
	char	*base;
	int		bits;

	base = "01";
	bits = 8;
	while (bits--)
	{
		bin_char[bits] = base[c % 2] - '0';
		c /= 2;
	}
}
